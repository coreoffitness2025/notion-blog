export function searchNutrition(
  query: string,
  locale: string,
  limit = 20
): NutritionItem[] {
  const q = query.toLowerCase().trim();
  if (!q) return DB.slice(0, limit);

  const lang = locale === "en" ? "en" : "ko";
  const names = NAMES[lang];

  // 동의어 확장
  const synonyms = SYNONYMS[q] || [];
  const allTerms = [q, ...synonyms.map((s) => s.toLowerCase())];

  // 점수 기반 검색
  const scored: { item: NutritionItem; score: number }[] = [];

  DB.forEach((item) => {
    const name = (names[item.id] || "").toLowerCase();
    const nameNoSpace = name.replace(/[\s_]/g, "");
    const qNoSpace = q.replace(/[\s_]/g, "");
    let score = 0;

    // 원본 쿼리 매칭
    if (name.includes(q) || nameNoSpace.includes(qNoSpace)) {
      score = 100 - Math.abs(name.length - q.length);
      if (name.startsWith(q) || nameNoSpace.startsWith(qNoSpace)) score += 10;
    }
    // 동의어 매칭
    else {
      for (const term of allTerms.slice(1)) {
        const termNoSpace = term.replace(/[\s_]/g, "");
        if (name.includes(term) || nameNoSpace.includes(termNoSpace)) {
          score = 80 - Math.abs(name.length - term.length);
          // 동의어 정확 매칭 보너스 (닭튀김 = "닭튀김" 그 자체)
          if (name === term || nameNoSpace === termNoSpace) score += 30;
          // 동의어로 시작하면 보너스 (닭튀김_양념 등)
          else if (name.startsWith(term) || nameNoSpace.startsWith(termNoSpace)) score += 15;
          break;
        }
      }
    }

    // 짧고 심플한 이름 보너스 (가정식 우선)
    if (score > 0 && !name.includes("(") && name.length <= 10) score += 8;
    // 완전 데이터 보너스
    if (score > 0 && item.dataQuality === "complete") score += 3;

    if (score > 0) scored.push({ item, score });
  });

  scored.sort((a, b) => b.score - a.score);
  return scored.slice(0, limit).map((s) => s.item);
}

/** Get popular items (complete data, reasonable serving size) for initial SSR */
export function getPopularNutrition(limit = 100): NutritionItem[] {
  const complete = DB.filter(
    (item) => item.dataQuality === "complete" && item.serving_size && item.serving_size < 1000
  );
  const kfda = complete.filter((i) => i.source === "kfda");
  const usda = complete.filter((i) => i.source === "usda");
  const usdaCount = Math.min(Math.floor(limit * 0.2), usda.length);
  const kfdaCount = limit - usdaCount;
  return [...kfda.slice(0, kfdaCount), ...usda.slice(0, usdaCount)];
}
